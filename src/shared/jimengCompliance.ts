import type { ComplianceIssue, ComplianceResult, ComplianceStatus } from "./types";

export const JIMENG_GUIDELINES_URL = "https://lf9-cdn-tos.draftstatic.com/obj/ies-hotsoon-draft/vco/jimeng_community_guidelines.html";
export const JIMENG_GUIDELINES_EFFECTIVE_DATE = "2024-11-18";

export const JIMENG_GUIDELINE_DIGEST = `《“即梦AI”社区自律公约》（2024-11-18生效）条款摘要：
第1至5条：暴力、违禁品、恐怖极端、违法犯罪及教唆帮助犯罪；
第6至7条：时政有害信息与时政不实信息；
第8至13条：自杀自残、网络暴力、人身自由、危险行为、隐私及其他人身权益；
第14至19条：色情淫秽、仇恨歧视、低俗媚俗、血腥暴力、惊悚恐怖、残忍麻木；
第20条：不实信息；
第21至23条：违背社会公德、传播不良价值导向、伤害民族情感；
第24条：知识产权保护；
第25至30条：未成年人性侵害、性化内容、不当牟利及其他权益侵害；
第31至34条：作弊垃圾信息、不当营销、恶意导流与欺骗；
第35至37条：账号、平台权益秩序与网络安全。`;

export function assessJimengCompliance(input: string): ComplianceResult {
  const text = normalize(input);
  const issues: ComplianceIssue[] = [];
  const add = (
    code: string,
    level: ComplianceIssue["level"],
    category: string,
    guideline: string,
    reason: string,
    suggestion: string
  ) => {
    if (!issues.some((issue) => issue.code === code)) issues.push({ code, level, category, guideline, reason, suggestion });
  };

  const minor = /(未成年|儿童|幼儿|婴儿|小学生|初中生|高中生|男童|女童|幼女|幼童|少年|少女|孩子)/.test(text);
  const sexual = /(性行为|性侵|猥亵|裸照|裸体|全裸|走光|性暗示|性挑逗|色情|淫秽|强奸|诱奸|援交|成人视频)/.test(text);
  const educationalContext = /(科普|教育|法治|医学|心理援助|反对|劝阻|救援|新闻报道|警示)/.test(text);
  const relationshipEthicsRisk =
    /(异地恋|恋爱中|情侣|夫妻|丈夫|妻子|已婚|婚内).{0,48}(各自|双方|同时|背着|瞒着|另有|另一个|不同|别的).{0,24}(伴侣|对象|恋人|情人|暧昧对象|家庭)/.test(text)
    || /(各自|双方).{0,18}(带着|另有|都有|同时交往).{0,12}(不同|另一个|别的|新的)?.{0,8}(伴侣|对象|恋人|情人).{0,40}(异地恋|恋爱中|情侣|夫妻|已婚)/.test(text)
    || /(婚内出轨|出轨|婚外情|偷情|劈腿|小三|脚踏两条船|双重恋爱|双向背叛|背叛婚姻)/.test(text)
    || /(丈夫|妻子|已婚|婚内).{0,24}(另一个家庭|第二个家庭|情人|小三|婚外对象)/.test(text);

  if (minor && sexual) {
    add("minor-sexual", "block", "未成年人权益", "第25至26条 未成年人性侵害相关内容", "内容涉及未成年人裸露、色情、性暗示或性侵害。", "删除所有未成年人相关的性化表达，不以成年化、虚拟化或动漫化规避。");
  } else if (sexual) {
    add("explicit-sexual", "block", "色情淫秽", "第14条 色情、淫秽内容", "内容包含露骨色情、性行为或性侵害表达。", "改为非裸露、非性化的人物关系和情绪表达。");
  }

  if (relationshipEthicsRisk) {
    add(
      "relationship-ethics",
      "review",
      "婚恋伦理与不良价值导向",
      "第22条 传播不良价值导向的内容",
      "题材以正在进行的亲密关系中另有伴侣、隐瞒双重关系或背叛为核心冲突，即使没有色情画面，也可能被平台按不良价值导向拒绝生成。",
      "优先改为“已经分手的前任携各自现任偶遇”，或把同行者明确改为同事、朋友；若必须保留背叛主题，应避免浪漫化并呈现坦白、结束关系或承担后果。"
    );
  }

  if (/(恐怖主义|极端主义|恐怖组织|圣战|恐袭)/.test(text)) {
    add("terror-extremism", "block", "暴力与犯罪", "第3条 恐怖主义与极端主义", "内容涉及宣扬、展示或美化恐怖主义、极端主义。", "移除相关组织、符号、主张和行动描写。");
  }

  if (/(购买|出售|交易|赠送|制作|自制|改装|藏匿|运输).{0,16}(枪支|弹药|炸药|爆炸物|毒品|剧毒物|管制刀具)|(枪支|弹药|炸药|爆炸物|毒品|剧毒物|管制刀具).{0,16}(购买|出售|交易|制作|教程|配方)/.test(text)) {
    add("controlled-items", "block", "违禁与管制物品", "第2条 违禁与管制物品", "内容涉及违禁品或管制物品的制作、交易或传播。", "删除制作、交易、获取方式及可操作细节。");
  }

  if (/(教程|步骤|方法|攻略|教你|如何).{0,18}(杀人|绑架|勒索|诈骗|盗窃|抢劫|制毒|爆炸|纵火|入侵|洗钱)|(教唆|组织|帮助|鼓励).{0,18}(犯罪|伤害|暴力)/.test(text)) {
    add("crime-facilitation", "block", "违法犯罪", "第4至5条 违法犯罪与教唆犯罪", "内容提供实施违法犯罪或伤害行为的方法。", "删除可执行步骤，改为守法教育、风险警示或阻止伤害。");
  }

  if (/(卖淫|嫖娼|人口贩卖|拐卖|买卖人口|强迫劳动|买卖婚姻|买卖人体器官|非法收养|非法送养)/.test(text)) {
    add(
      "personal-freedom",
      educationalContext ? "review" : "block",
      "人身自由",
      "第10条 侵犯人身自由",
      "内容涉及人口买卖、强迫劳动、器官交易或其他侵犯人身自由的行为。",
      "删除展示、宣扬或美化内容；公益警示只保留制止、救助和依法处理结果。"
    );
  }

  if (/(自杀|自残|割腕|跳楼|上吊|吞药|烧炭)/.test(text)) {
    add(
      "self-harm",
      educationalContext ? "review" : "block",
      "自杀自残",
      "第8条 自杀、自残",
      educationalContext ? "内容涉及自杀或自残，即使出于警示目的也需要谨慎表达。" : "内容展示、宣扬或诱导自杀、自残。",
      "避免呈现方法、过程和伤口，改为求助、干预、救援和康复表达。"
    );
  }

  if (/(肢解|断肢|开膛|爆头|血肉模糊|尸块|内脏外露|喷血|剥皮|碎尸|挖眼)/.test(text)) {
    add("graphic-gore", "block", "血腥暴力", "第17条 血腥暴力内容", "内容含有直观、重口味的血腥或残肢细节。", "删除伤口、血液和身体破坏细节，以画外反应或非直观结果替代。");
  } else if (/(殴打|打架|斗殴|刺伤|枪击|砍伤|绑架|虐待|杀人|凶杀|爆炸|纵火|酷刑)/.test(text)) {
    add("violence", "review", "暴力行为", "第1条 暴力与第17条 血腥暴力内容", "内容包含暴力、伤害或犯罪情节，平台可能拒绝直接展示。", "删除伤害过程和美化表达，改为冲突前后、制止行为或非暴力结果。");
  }

  if (/(人肉搜索|开盒|曝光|泄露|公布).{0,16}(身份证|手机号|电话|住址|家庭地址|银行卡|病历|行踪|隐私)|\b1[3-9]\d{9}\b|\b\d{17}[\dXx]\b|\b\d{16,19}\b/.test(text)) {
    add("privacy", "block", "隐私与个人信息", "第12条 侵犯隐私与个人信息", "内容包含或要求曝光可识别个人信息、隐私或行踪。", "删除真实身份、联系方式、住址、财务和医疗信息，使用不可追溯的虚构信息。");
  }

  if (/(网暴|网络暴力|人肉搜索|开盒|挂人|辱骂|侮辱|诽谤|造黄谣|恶意揣测|公开羞辱|恶意骚扰)/.test(text)) {
    add(
      "cyberbullying",
      educationalContext ? "review" : "block",
      "网络暴力与名誉权",
      "第9条 网络暴力与第13条 其他侵犯人身权益",
      "内容包含针对个人或群体的辱骂、诽谤、曝光或骚扰。",
      "删除攻击、曝光和煽动围攻的表达；警示内容聚焦制止、取证和求助。"
    );
  }

  if (/(仇恨|歧视|低等|清除|驱逐|消灭).{0,16}(性别|女性|男性|老人|残疾人|地域|民族|种族|宗教|国籍|性别认同)|(地域黑|种族歧视|民族仇恨)/.test(text)) {
    add("hate-discrimination", "block", "仇恨与歧视", "第15条 仇恨与歧视性言论", "内容基于受保护身份贬损、排斥或煽动仇恨。", "删除群体攻击和优劣判断，改为平等、尊重的个体叙事。");
  }

  if (/(擦边|性挑逗|软色情|透视装|刻意走光|胸部特写|臀部特写|舔舐|挑逗姿势)/.test(text)) {
    add("sexualized-lowbrow", "block", "低俗媚俗", "第16条 低俗媚俗内容", "内容以性暗示、身体凝视或低俗方式吸引注意。", "改用非性化服装、构图和人物互动。");
  }

  if (/(跳脸惊吓|恐怖尸体|腐烂尸体|鬼脸贴脸|密集虫体|恶心呕吐|惊悚音效|极度恐怖)/.test(text)) {
    add("horror-shock", "block", "惊悚恐怖", "第18条 惊悚恐怖内容", "内容可能造成强烈惊恐、焦虑、厌恶或生理不适。", "降低感官刺激，不使用突脸、尸体、腐败、尖锐恐怖音效等元素。");
  }

  if (/(虐猫|虐狗|虐待动物|伤害动物|嘲笑苦难|取笑伤者|围观死亡|漠视生命|以受辱为乐)/.test(text)) {
    add(
      "cruelty",
      "block",
      "残忍麻木",
      "第19条 残忍麻木内容",
      "内容伤害动物、消费他人苦难或以受害者的屈辱为乐。",
      "删除伤害、嘲笑和围观表达，改为救助、关怀、制止或承担后果。"
    );
  }

  if (/(假新闻|伪纪实|冒充官方|虚构新闻|捏造事实|造谣|谣言|假冒记者|假冒媒体)/.test(text)) {
    add("misinformation", "block", "不实信息", "第20条 不实信息与第34条 欺骗行为", "内容可能将虚构事件包装为真实事实、新闻或官方信息。", "明确标注虚构创作，不冒充媒体、机构或真实事件记录。");
  }

  if (/(虚假慈善|伪慈善|编造捐款|虚构捐赠|编造悲惨身世|卖惨骗捐)/.test(text)) {
    add("false-charity", "block", "虚假慈善", "第21条 违背社会公德的内容", "内容通过虚构捐赠或悲惨经历制造伪善、骗取关注或财物。", "删除虚构慈善、骗捐和卖惨导向，只使用可核验的真实公益信息。");
  }

  if (/(国家领导人|党政军机关|国旗|国徽|国歌|军旗|军徽|人民币|英雄烈士|重大灾情|重大突发事件)/.test(text)) {
    add("public-affairs", "review", "时政与特殊标志", "第6至7条 时政有害及不实信息", "内容涉及敏感时政人物、事件或具有特殊含义的国家象征。", "避免虚构、恶搞、歪曲或未经来源核验的表达；必要时提供真实来源与时间地点。");
  }

  if (/(飙车|危险驾驶|逆行|高楼边缘|卧轨|窒息挑战|点火挑战|危险恶作剧|翻越护栏|攀爬高压线)/.test(text)) {
    add("dangerous-behavior", "review", "危险行为", "第11条 危险行为", "内容展示可能引发伤亡或模仿的危险行为。", "改为有专业保护的安全场景，删除操作细节并明确风险警示。");
  }

  if (/(赌博|下注|博彩|赌场|刷单|返利|网络贷款|中奖链接|杀猪盘|电信诈骗|高额回报).{0,20}(教程|赚钱|推广|引流|加入|扫码|联系|稳赚|获利|提现)|(教你|带你).{0,16}(赌博|刷单|诈骗|套现)/.test(text)) {
    add("fraud-gambling", "block", "诈骗与不当营销", "第32至34条 不当营销、恶意导流与欺骗", "内容宣传或引导赌博、诈骗、刷单返利等高风险行为。", "删除交易、导流、收益承诺和联系方式，改为反诈警示。");
  }

  if (/(二维码|微信号|手机号|外部链接|站外联系|扫码).{0,18}(关注|加我|购买|领取|返利|下注|赚钱|资源)/.test(text)) {
    add("malicious-traffic", "block", "恶意导流", "第33条 恶意导流", "内容通过联系方式、二维码或外链诱导站外交易或获取违规内容。", "删除站外联系方式、二维码、外链和诱导性话术。");
  }

  if (/(仿照|复刻|照搬|一模一样|换脸|冒用).{0,20}(明星|艺人|名人|他人肖像|电影|电视剧|动漫角色|品牌|商标|作品)|(未经授权|盗版|盗用).{0,16}(音乐|角色|画面|作品|商标|肖像)/.test(text)) {
    add("rights-ip", "review", "肖像与知识产权", "第13条 人身权益与第24条 知识产权保护", "内容可能使用未经授权的肖像、角色、商标或作品。", "改用原创或已授权素材，并在使用前确认肖像权、著作权和商标授权。");
  }

  if (minor && /(吸烟|饮酒|赌博|纹身|危险驾驶|打架|犯罪|虐待|自残|跳楼|绑架)/.test(text)) {
    add("minor-harm", "block", "未成年人权益", "第28至30条 未成年人保护", "内容描绘或可能诱导未成年人实施危险、违法或有害行为。", "删除未成年人参与的不安全行为，改为保护、教育或救助场景。");
  }

  if (minor && /(带货|直播赚钱|商业演出|陪酒|牟利|赚钱工具|引流|广告代言|强迫表演)/.test(text)) {
    add("minor-exploitation", "block", "未成年人权益", "第27条 不当利用未成年人牟取利益", "内容可能利用未成年人进行商业牟利或不符合其年龄的表演。", "删除利用未成年人牟利、引流或强迫表演的设计，改为符合年龄并获得监护同意的非商业场景。");
  }

  if (/(恶搞灾难|嘲笑受害者|羞辱他人|侮辱逝者|破坏陵墓|随地吐痰|乱扔垃圾|逃票|翻越闸机)/.test(text)) {
    add("public-morality", "review", "公序良俗", "第21至22条 社会公德与价值导向", "内容可能美化不文明行为、羞辱他人或消费苦难。", "删除恶搞、美化和模仿导向，改为尊重生命、承担后果的表达。");
  }

  if (/(宣扬|美化|鼓吹|推崇).{0,12}(拜金|炫富|不劳而获|违法致富|暴力解决|歧视|羞辱|虐待|逃票|破坏公物|消极颓废)/.test(text)) {
    add("negative-values", "review", "不良价值导向", "第22条 传播不良价值导向的内容", "内容可能肯定或美化违法、不文明、消极颓废的价值选择。", "取消赞许和模仿导向，呈现真实后果、责任或积极解决方式。");
  }

  if (/(伤害民族感情|侮辱中华文化|歪曲民族历史|贬损民族英雄|亵渎民族传统)/.test(text)) {
    add("national-sentiment", "block", "民族情感", "第23条 严重伤害民族情感的内容", "内容可能歪曲、侮辱民族历史、文化、传统或代表人物。", "删除侮辱、亵渎和歪曲表达，以尊重史实和文化传统的方式创作。");
  }

  if (/(刷赞|刷粉|刷播放量|刷评论|批量注册|批量发帖|批量发布|群控账号|垃圾信息|自动点赞|恶意抓取数据)/.test(text)) {
    add("spam-cheating", "block", "作弊与垃圾信息", "第31条 作弊行为与垃圾信息", "内容涉及刷量、批量账号、自动化互动或垃圾信息传播。", "删除刷量、群控、批量注册和自动化滥用平台的行为设计。");
  }

  if (/(虚假宣传|夸大宣传|夸大疗效|包治百病|百分百治愈|稳赚不赔|保证中奖|零风险高回报|假冒品牌)/.test(text)) {
    add("false-marketing", "block", "虚假宣传", "第32条 不当营销与虚假宣传", "内容包含不可证实的效果承诺、收益保证或误导性商业宣传。", "删除绝对化承诺、伪造背书和误导性对比，只保留可核验信息。");
  }

  if (/(假冒|仿冒|冒充).{0,16}(即梦官方|即梦工作人员|政府机关|新闻媒体|公益组织|银行客服|平台客服)/.test(text)) {
    add("impersonation", "block", "冒充与欺骗", "第34至36条 欺骗行为及平台秩序", "内容要求冒充官方机构、媒体、平台或工作人员。", "删除冒充身份、仿造标识和诱导信任的设计，使用清晰的虚构身份。");
  }

  const status: ComplianceStatus = issues.some((issue) => issue.level === "block")
    ? "block"
    : issues.length
      ? "review"
      : "pass";
  const summary = status === "pass"
    ? "未发现《即梦AI社区自律公约》中的明确风险，可进入 Prompt 生成。"
    : status === "review"
      ? `发现 ${issues.length} 项平台审核风险，已提醒；你仍可继续生成。`
      : `发现 ${issues.length} 项高风险内容，已提醒；本工具不会限制生成。`;

  return {
    status,
    canGenerate: true,
    summary,
    issues,
    guidelineUrl: JIMENG_GUIDELINES_URL
  };
}

function normalize(value: string) {
  return String(value || "")
    .replace(/[\r\n\t]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}
